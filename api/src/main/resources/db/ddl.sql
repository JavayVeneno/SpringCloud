DROP TABLE IF EXISTS  `cloud_user` ;
CREATE TABLE `cloud_user` (
  `ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT 'ID',
  `USER_NAME` varchar(32) DEFAULT NULL COMMENT '用户名',
  `IS_DELETED` tinyint(4) DEFAULT NULL COMMENT '删除',
  `CREATED_BY` varchar(32) DEFAULT NULL COMMENT '创建人',
  `UPDATED_BY` varchar(32) DEFAULT NULL COMMENT '修改人',
  `DATE_CREATED` datetime(6) DEFAULT NULL COMMENT '创建时间',
  `DATE_UPDATED` datetime(6) DEFAULT NULL COMMENT '修改时间',
  `REMARK` varchar(255) DEFAULT NULL COMMENT '备注',
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=4144 DEFAULT CHARSET=utf8;